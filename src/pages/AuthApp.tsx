import { useState } from "react";
import { Link, useSearchParams } from "react-router-dom";
import { Button } from "@/components/ui/button";
import { Input } from "@/components/ui/input";
import { Label } from "@/components/ui/label";

const AuthApp = () => {
  const [email, setEmail] = useState("");
  const [loading, setLoading] = useState(false);
  const [searchParams] = useSearchParams();

  // we get the scheme from the url params apparently look this up
  const deeplinkScheme = searchParams.get("scheme") || searchParams.get("callback") || "heldapp://";

  // Deeplink to app's auth method --> this is where we send the user to the app to authenticate we need to register w os here
  const handleDeeplink = (method: string) => {
    try {
      // Construct deeplink URL to call app's method
      // Format: heldapp://auth/{method} or heldapp://{method}
      const deeplinkUrl = `${deeplinkScheme}auth/${method}`;
      
      // Attempt to deeplink to the app
      window.location.href = deeplinkUrl;

      // error
      setTimeout(() => {
        alert("Please make sure the Held app is installed to continue with authentication.");
      }, 1000);
    } catch (err) {
      console.error("Deeplink error:", err);
      alert("Unable to open the app. Please make sure the Held app is installed.");
    }
  };
  // this is the 3 diff login methods we need to establish these endpoints
  const handleContinue = (e: React.FormEvent) => {
    e.preventDefault();
    setLoading(true);
    // Deeplink to app's email auth method
    handleDeeplink("email");
    setTimeout(() => setLoading(false), 1000);
  };

  const handleGoogleAuth = () => {
    setLoading(true);
    // Deeplink to app's Google auth method - app will handle PKCE flow
    handleDeeplink("google");
    setTimeout(() => setLoading(false), 500);
  };

  const handleGithubAuth = () => {
    setLoading(true);
    // Deeplink to app's GitHub auth method - app will handle PKCE flow
    handleDeeplink("github");
    setTimeout(() => setLoading(false), 500);
  };

 // frontend
  return (
    <div className="min-h-screen flex">
      {/* Left Column - Sign In Form */}
      <div className="flex-1 flex items-center justify-center bg-background p-8">
        <div className="w-full max-w-md">
          {/* Logo */}
          <Link to="/" className="flex items-center gap-2 mb-8 hover:opacity-80 transition-opacity">
            <img src="/held_icon_white.png" alt="Held" className="h-8 w-8" />
            <span className="text-2xl font-semibold text-foreground">Held</span>
          </Link>

          {/* Welcome Message */}
          <h1 className="text-4xl font-semibold mb-3 text-foreground">
            Welcome to Held
          </h1>
          <p className="text-muted-foreground mb-8 text-lg">
            Your AI-powered terminal for command recall and team knowledge. Sign in to access your dashboard.
          </p>

          {/* Email Form */}
          <form onSubmit={handleContinue} className="space-y-6">
            <div>
              <Label htmlFor="email" className="text-foreground">Email</Label>
              <Input
                id="email"
                type="email"
                value={email}
                onChange={(e) => setEmail(e.target.value)}
                placeholder="Enter your email"
                className="mt-2 bg-background border-border"
                required
              />
            </div>

            <Button
              type="submit"
              className="w-full bg-foreground text-background hover:bg-foreground/90"
              size="lg"
              disabled={loading}
            >
              {loading ? "Processing..." : "Continue"}
            </Button>
          </form>

          {/* Separator */}
          <div className="relative my-6">
            <div className="absolute inset-0 flex items-center">
              <span className="w-full border-t border-border" />
            </div>
            <div className="relative flex justify-center text-xs uppercase">
              <span className="bg-background px-2 text-muted-foreground">OR</span>
            </div>
          </div>

          {/* OAuth Buttons */}
          <div className="space-y-3">
            <Button
              onClick={handleGoogleAuth}
              variant="outline"
              className="w-full border-border hover:bg-secondary"
              size="lg"
              disabled={loading}
            >
              <svg className="mr-2 h-5 w-5" viewBox="0 0 24 24">
                <path
                  fill="currentColor"
                  d="M22.56 12.25c0-.78-.07-1.53-.2-2.25H12v4.26h5.92c-.26 1.37-1.04 2.53-2.21 3.31v2.77h3.57c2.08-1.92 3.28-4.74 3.28-8.09z"
                />
                <path
                  fill="currentColor"
                  d="M12 23c2.97 0 5.46-.98 7.28-2.66l-3.57-2.77c-.98.66-2.23 1.06-3.71 1.06-2.86 0-5.29-1.93-6.16-4.53H2.18v2.84C3.99 20.53 7.7 23 12 23z"
                />
                <path
                  fill="currentColor"
                  d="M5.84 14.09c-.22-.66-.35-1.36-.35-2.09s.13-1.43.35-2.09V7.07H2.18C1.43 8.55 1 10.22 1 12s.43 3.45 1.18 4.93l2.85-2.22.81-.62z"
                />
                <path
                  fill="currentColor"
                  d="M12 5.38c1.62 0 3.06.56 4.21 1.64l3.15-3.15C17.45 2.09 14.97 1 12 1 7.7 1 3.99 3.47 2.18 7.07l3.66 2.84c.87-2.6 3.3-4.53 6.16-4.53z"
                />
              </svg>
              Continue with Google
            </Button>

            <Button
              onClick={handleGithubAuth}
              variant="outline"
              className="w-full border-border hover:bg-secondary"
              size="lg"
              disabled={loading}
            >
              <svg className="mr-2 h-5 w-5" fill="currentColor" viewBox="0 0 24 24">
                <path d="M12 0c-6.626 0-12 5.373-12 12 0 5.302 3.438 9.8 8.207 11.387.599.111.793-.261.793-.577v-2.234c-3.338.726-4.033-1.416-4.033-1.416-.546-1.387-1.333-1.756-1.333-1.756-1.089-.745.083-.729.083-.729 1.205.084 1.839 1.237 1.839 1.237 1.07 1.834 2.807 1.304 3.492.997.107-.775.418-1.305.762-1.604-2.665-.305-5.467-1.334-5.467-5.931 0-1.311.469-2.381 1.236-3.221-.124-.303-.535-1.524.117-3.176 0 0 1.008-.322 3.301 1.23.957-.266 1.983-.399 3.003-.404 1.02.005 2.047.138 3.006.404 2.291-1.552 3.297-1.23 3.297-1.23.653 1.653.242 2.874.118 3.176.77.84 1.235 1.911 1.235 3.221 0 4.609-2.807 5.624-5.479 5.921.43.372.823 1.102.823 2.222v3.293c0 .319.192.694.801.576 4.765-1.589 8.199-6.086 8.199-11.386 0-6.627-5.373-12-12-12z"/>
              </svg>
              Continue with GitHub
            </Button>
          </div>
        </div>
      </div>

      {/* Right Column - Held Logo */}
      <div 
        className="hidden lg:flex flex-1 items-center justify-center p-12"
        style={{
          background: "linear-gradient(180deg, hsl(0 0% 8%) 0%, hsl(0 0% 4%) 100%)",
        }}
      >
        <div className="text-center">
          <div className="flex items-center justify-center gap-6 mb-8">
            <img 
              src="/held_icon_white.png" 
              alt="Held" 
              className="h-32 w-32 object-contain"
            />
            <h2 className="text-8xl font-semibold text-foreground tracking-tight">
              Held
            </h2>
          </div>
        </div>
      </div>
    </div>
  );
};

export default AuthApp;

